package vo;

/**
 *
 * @author Labing
 */
public class HabitacionVO {
    private int numero;
    private int piso;
    private int capacidad;
    private double precio;

    public HabitacionVO() {
    
    }

    public HabitacionVO(int numero, int piso, int capacidad, double precio) {
        this.numero = numero;
        this.piso = piso;
        this.capacidad = capacidad;
        this.precio = precio;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getPiso() {
        return piso;
    }

    public void setPiso(int piso) {
        this.piso = piso;
    }

    public int getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(int capacidad) {
        this.capacidad = capacidad;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    
}
